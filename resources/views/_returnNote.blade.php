<h4 class="mb-5">W przypadku, gdy nie jesteś w pełni zadowolony z zakupionego produktu, oferujemy możliwość jego zwrotu oraz
    zwrotu poniesionych kosztów. Oto kroki, które należy podjąć:</h4>
<h5 class="mb-1 text-lg font-bold">1. Okres na Zwrot:</h5>
<p class="mb-5">Masz prawo zwrócić produkt w ciągu 14 dni kalendarzowych od daty otrzymania przesyłki.</p>
<h5 class="mb-1 text-lg font-bold">2. Warunki Zwrotu:</h5>
<p class="mb-5">Produkt musi być zwrócony w stanie niezmienionym, nieuszkodzony i w oryginalnym opakowaniu, wraz z wszystkimi
    akcesoriami.</p>
<h5 class="mb-1 text-lg font-bold">3. Proces Zwrotu:</h5>
<p class="mb-5">Aby zainicjować zwrot, skontaktuj się z naszą obsługą klienta za pośrednictwem e-maila lub telefonu. Podaj numer
    zamówienia oraz powód zwrotu. Otrzymasz wtedy szczegółowe instrukcje dotyczące procesu zwrotu.</p>
<h5 class="mb-1 text-lg font-bold">4. Zwrot Pieniędzy: </h5>
<p class="mb-5">Po otrzymaniu i sprawdzeniu zwróconego produktu, przystąpimy do procesu zwrotu pieniędzy. Zwrot zostanie
    zrealizowany na dane bankowe podane przez ciebie w treści maila.</p>
<h5 class="mb-1 text-lg font-bold">5. Czas Realizacji Zwrotu:</h5>
<p class="mb-5">Zwrot pieniędzy zostanie przeprowadzony w ciągu 14 dni roboczych od daty otrzymania zwróconego produktu.</p>

